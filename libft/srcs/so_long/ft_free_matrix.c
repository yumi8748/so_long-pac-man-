#include "../../inc/libft.h"

void	ft_free_matrix(char ***map)
{
	int	i;

	i = 0;
	while (map && map[0] && map[0][i])
	{
		free(map[0][i]);
		i++;
	}
	if (map)
	{
		free(map[0]);
		*map = NULL;
	}
}
