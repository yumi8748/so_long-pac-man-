#include "../../inc/libft.h"

int	ft_sqrt(int nb)
{
	int	n;

	n = 0;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (n * n <= nb && n <= 46340)
	{
		if (n * n == nb)
			return (n);
		n++;
	}
	return (0);
}
